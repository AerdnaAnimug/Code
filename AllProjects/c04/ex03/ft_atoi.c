int	ft_atoi(char *str)
{
	int		i;
	int		minus;
	int		n;

	minus = 0;
	i = 0;
	n = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10;
		n = n + (str[i] - 48);
		i++;
	}
	if (minus % 2 == 1)
		n = -n;
	return (n);
}
